def build_rule_context(question: str | None) -> dict:
    question_type = classify_question(question or "")
    names = list(ISSUE_CARD_NAMES["通用"])
    if question_type != "通用":
        names.extend(ISSUE_CARD_NAMES[question_type])
    cards = [RULE_CARDS[name] for name in names if name in RULE_CARDS]
    return {
        "question_type": question_type,
        "selection_rule": "规则卡片只提供分析依据；必须结合课盘字段，未由工具输出或不可定位者只能作为待校验辅助。",
        "cards": cards,
    }


def classify_question(question: str) -> str:
    normalized = question.strip()
    for question_type, keywords in QUESTION_KEYWORDS:
        if any(keyword in normalized for keyword in keywords):
            return question_type
    return "通用"
